const int MOD = 1000000007;

void solve(int T)
{
	ll t3, tl3, s;
	cin>>t3>>tl3>>s;
	ll n = (2*s)/(t3 + tl3);
	ll d = (tl3 - t3)/(n-5);
	ll a = t3 - 2*d;
	cout<<n<<"\n";
	for(ll i=0;i<n;i++)
	cout<<(a + i*d)<<" \n"[i == n-1];
	return;	
}

